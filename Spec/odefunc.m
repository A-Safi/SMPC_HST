xd(11)=-(u(4)/m(4)+k/m(4)*(-x(4)-l)-k/m(4)*(-x(5)-l)-(c(1)+c(2)*v_abs(4)+c(3)*v_abs(4)^2) -u(5)/m(5)-k/m(5)*(-x(5)-l)+k/m(5)*(-x(6)-l)+(c(1)+c(2)*v_abs(5)+c(3)*v_abs(5)^2));
xd(12)=-(u(5)/m(5)+k/m(5)*(-x(5)-l)-k/m(5)*(-x(6)-l)-(c(1)+c(2)*v_abs(5)+c(3)*v_abs(5)^2) -u(6)/m(6)-k/m(6)*(-x(6)-l)                 +(c(1)+c(2)*v_abs(6)+c(3)*v_abs(6)^2));
end
