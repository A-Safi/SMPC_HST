function [xd]=odefunc(x,u,l,m,c,k)
xd=zeros(12,1);
xd(1)=x(7);
xd(2)=x(8);
xd(3)=x(9);
xd(4)=x(10);
xd(5)=x(11);
xd(6)=x(12);

v_abs=[x(7) sum(x(7:8)) sum(x(7:9)) sum(x(7:10)) sum(x(7:11)) sum(x(7:12))];

xd(7)=   u(1)/m(1)                 -k/m(1)*(-x(2)-l)-(c(1)+c(2)*v_abs(1)+c(3)*v_abs(1)^2);
xd(8)= -(u(1)/m(1)                 -k/m(1)*(-x(2)-l)-(c(1)+c(2)*v_abs(1)+c(3)*v_abs(1)^2) -u(2)/m(2)-k/m(2)*(-x(2)-l)+k/m(2)*(-x(3)-l)+(c(1)+c(2)*v_abs(2)+c(3)*v_abs(2)^2));
